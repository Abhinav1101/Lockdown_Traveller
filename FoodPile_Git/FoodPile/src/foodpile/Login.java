    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        int res=0;
        
        try{
            Uname = loginusername.getText();
            Pass = loginpass.getText();
            
            Socket s = new Socket("localhost",6666);
            DataOutputStream dout = new DataOutputStream(s.getOutputStream());
            dout.writeUTF("userLogin");
            
            dout.writeUTF(Uname);
            dout.writeUTF(Pass);
            dout.writeUTF("null");
            dout.flush();

            DataInputStream din = new DataInputStream(s.getInputStream());
            res =  din.read();
            dout.close();
            s.close();
            
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
        System.out.println("result = "+res);
        
        if(res==1){
            if(remember_me.isSelected())
            {
                     
                prefs.put("Username", Uname);
                prefs.put("Password",Pass);
            }
            else
            {
                String prev_username=prefs.get("Username","");
               if(!prev_username.equals(Uname))
               {
                    prefs.put("Username","");
                    prefs.put("Password","");
                }
            }
            JOptionPane.showMessageDialog(null, "success");
            loggedInUser=Uname;

            try{
                Class.forName(classForName);
                Connection con = DriverManager.getConnection(getConnection, username,password);
                PreparedStatement ps=con.prepareStatement("select * from users where Username=?");
                ps.setString(1, loggedInUser);
                ResultSet rs=ps.executeQuery();
                System.out.println("done and found");
                String st="";
                while(rs.next())
                {
                    st=rs.getString("verify");
                }
                System.out.println(st);
                String stt="Yes";
                    if(st.equals(stt))
                    {
                        DashboardFood obj=new DashboardFood();
                        obj.setVisible(true);
                        setVisible(false);
                    }  
                    else
                    {
                        Verifymail obj=new Verifymail();
                        obj.setVisible(true);
                        setVisible(false);
                    }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            } 

        }
        else if(res==2){
            JOptionPane.showMessageDialog(this, "There cannot be space in Username");
        }
        
        else{
            JOptionPane.showMessageDialog(null, "failure");
        }
    }//GEN-LAST:event_loginActionPerformed
    
    
    
